package com.home.everispushdemo

import android.content.Intent
import android.util.Log
import com.google.firebase.messaging.FirebaseMessagingService
import com.google.firebase.messaging.RemoteMessage

class MyFirebaseMessagingService : FirebaseMessagingService() {

    val TAG = MyFirebaseMessagingService::class.java.simpleName

    override fun onMessageReceived(remoteMessage: RemoteMessage) {
        super.onMessageReceived(remoteMessage)
        handleMessage(remoteMessage)
    }

    private fun handleMessage(remoteMessage: RemoteMessage) {

        if (remoteMessage.data.isNotEmpty()) {
            val intent = Intent("Data-Push-Event")
            intent.putExtra("value", remoteMessage.data["value"])
            sendBroadcast(intent)
        }

        remoteMessage.notification?.let {
            val intent = Intent("Message-Push-Event")
            intent.putExtra("body", it.body);
            intent.putExtra("title", it.title)
            sendBroadcast(intent)
        }
    }

    /**
     * Called if InstanceID token is updated. This may occur if the security of
     * the previous token had been compromised. Note that this is called when the InstanceID token
     * is initially generated so this is where you would retrieve the token.
     */
    override fun onNewToken(p0: String) {
        super.onNewToken(p0)
        Log.d(TAG, "new token: $p0")
    }
}